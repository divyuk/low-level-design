package model;

public class Address {
    String apartments;
    String area;
    String city;
    String state;
    String country;
    String landmark;

    public Address(String apartments, String area, String city, String state, String country, String landmark) {
        this.apartments = apartments;
        this.area = area;
        this.city = city;
        this.state = state;
        this.country = country;
        this.landmark = landmark;
    }
}
